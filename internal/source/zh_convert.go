// foldTraditionalToSimplified 把繁体字按字符折叠成简体,用于搜索归一化(匹配/去重)。
//
// 繁→简多为「多对一」,逐字映射个体置信度高,因此本表只做无外部依赖的繁→简折叠:
// 简体查询与繁体标题(或反之)会折叠到同一规范键,避免「鬼滅之刃 / 鬼灭之刃」被当成两部作品,
// 也让简体关键词能命中繁体标题。表为常见影视标题用字的精选子集,未覆盖的字原样保留(不致误伤)。
func foldTraditionalToSimplified(s string) string {
	if s == "" {
		return s
	}
	var b strings.Builder
	changed := false
	for _, r := range s {
		if mapped, ok := tradToSimpRunes[r]; ok {
			b.WriteRune(mapped)
			changed = true
			continue
		}
		b.WriteRune(r)
	}
	if !changed {
		return s
	}
	return b.String()
}

